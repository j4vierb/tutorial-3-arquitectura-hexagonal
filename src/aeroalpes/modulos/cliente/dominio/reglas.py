class NombresYApellidosValidos(ReglaNegocio):
    usuario: Usuario

    def __init__(self, usuario: Usuario, mensaje = "El nombre y el apellido no pueden ser nulos."):
        super().__init__(mensaje)
        self.usuario = usuario
    
    def contiene_caracteres_especiales(self, s: str) -> bool:
        especiales = "!@#$%^&*()-+?_=,<>/"
        return any(char in especiales for char in s)

    def es_valido(self) -> bool:
        if (self.usuario.nombre.nombres is None or self.usuario.nombre.apellidos is None) \
            or (self.contiene_caracteres_especiales(self.usuario.nombre.nombres) or self.contiene_caracteres_especiales(self.usuario.nombre.apellidos)):
            return False
        return True

