class CedulaValida(ReglaNegocio):
    usuario: ClienteNatural

    def __init__(self, usuario: ClienteNatural, mensaje = "La cédula no es válida. Mayor a 0 y menor a 10 digitos"):
        super().__init__(mensaje)
        self.usuario = usuario
    
    def es_valido(self) -> bool:
        if self.usuario.cedula.numero <= 0 or self.usuario.cedula.numero / 10e10 > 1:
            return False
        return True
    
